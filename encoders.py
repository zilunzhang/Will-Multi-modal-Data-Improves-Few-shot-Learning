import torch
from torch import Tensor
import torch.nn as nn
from typing import Type, Any, Callable, Union, List, Optional
from sentence_transformers import SentenceTransformer, models


class SentenceEncoder(nn.Module):
    def __init__(self, emb_size, sentence_len=10):
        super().__init__()
        device = 'cuda' if torch.has_cuda else 'cpu'

        self.encoder = SentenceTransformer('distilbert-base-uncased', device=device)
        for param in self.encoder.parameters():
            param.requires_grad = False

        self.embedding_dimension = 768
        self.out_features = emb_size
        self.fc = nn.Linear(self.embedding_dimension, self.out_features)
        self.merge_tenshot_fc = nn.Conv1d(sentence_len, 1, 1)

    def forward(self, x, id_sentence_mapping, fusion_method):
        device = torch.device('cuda' if x.is_cuda else 'cpu')
        num_instance, num_sentence_per_instance = x.shape
        # x: (5, 10)
        self.fc = self.fc.to(device)
        self.merge_tenshot_fc = self.merge_tenshot_fc.to(device)

        # x: (50, )
        x = torch.reshape(x, (-1, ))
        x_sentence = []
        for item in x:
            key = str(int(item.cpu().detach().numpy()))
            sentence = id_sentence_mapping[key]
            x_sentence.append(sentence)
        x = x_sentence
        # x: (50, 768)
        x = self.encoder.encode(x, convert_to_tensor=True)
        # x: (50, 800)
        x = self.fc(x)
        # x: (5, 10, 800)
        out_att = torch.reshape(x, (num_instance, num_sentence_per_instance, -1))
        # # x: (5, 800)
        out = self.merge_tenshot_fc(out_att).squeeze(1)
        if fusion_method == "attention":
            return out, out_att
        else:
            return out


def conv_block(in_channels, out_channels):
    bn = nn.BatchNorm2d(out_channels)
    nn.init.uniform_(bn.weight) # for pytorch 1.2 or later
    return nn.Sequential(
        nn.Conv2d(in_channels, out_channels, 3, padding=1),
        bn,
        nn.ELU(),
        nn.MaxPool2d(2)
    )


class ConvNet(nn.Module):

    def __init__(self, emb_size, x_dim=3, hid_dim=32, z_dim=32):
        super().__init__()
        self.encoder = nn.Sequential(
            conv_block(x_dim, hid_dim),
            conv_block(hid_dim, hid_dim),
            conv_block(hid_dim, hid_dim),
            conv_block(hid_dim, z_dim),
        )
        self.out_channels = 800
        self.fc = nn.Linear(self.out_channels, emb_size)
        self.image_conv1x1 = nn.Conv1d(1, 10, 1)

    def forward(self, x, fusion_method):
        x = self.encoder(x)
        # (5, 1, 32 * 5 * 5)
        flatten_x = x.view(x.size(0), -1)

        # for attention
        # (5, 1, 800) -> (5, 10, 800)
        att_flatten_x = self.image_conv1x1(flatten_x.unsqueeze(1))

        # (5, 1, 800) -> (5, 1, 128)
        out = self.fc(flatten_x)

        if fusion_method == "attention":
            out_att = self.fc(att_flatten_x)
            return out, out_att
        else:
            return out
    

class ResNet12Block(nn.Module):
    """
    ResNet Block
    """
    def __init__(self, inplanes, planes):
        super(ResNet12Block, self).__init__()

        self.conv1 = nn.Conv2d(inplanes, planes, kernel_size=1, bias=False)
        self.bn1 = nn.BatchNorm2d(planes)
        self.conv2 = nn.Conv2d(planes, planes, kernel_size=3, stride=1,
                               padding=1, bias=False)
        self.bn2 = nn.BatchNorm2d(planes)
        self.conv3 = nn.Conv2d(planes, planes, kernel_size=1, bias=False)
        self.bn3 = nn.BatchNorm2d(planes)
        self.relu = nn.LeakyReLU(negative_slope=0.2, inplace=True)

        self.conv = nn.Conv2d(inplanes, planes, kernel_size=1, bias=False)
        self.bn = nn.BatchNorm2d(planes)
        self.maxpool = nn.MaxPool2d(kernel_size=3, stride=2, padding=1)

    def forward(self, x):
        residual = x
        residual = self.conv(residual)
        residual = self.bn(residual)

        out = self.conv1(x)
        out = self.bn1(out)
        out = self.relu(out)

        out = self.conv2(out)
        out = self.bn2(out)
        out = self.relu(out)

        out = self.conv3(out)
        out = self.bn3(out)

        out += residual
        out = self.relu(out)
        out = self.maxpool(out)
        return out


class ResNet12(nn.Module):
    """
    ResNet12 Backbone
    """
    def __init__(self, emb_size, block=ResNet12Block, cifar_flag=False):
        super(ResNet12, self).__init__()
        cfg = [64, 128, 256, 512]
        # layers = [1, 1, 1, 1]
        iChannels = int(cfg[0])
        self.conv1 = nn.Conv2d(3, iChannels, kernel_size=3, stride=1, padding=1, bias=False)
        self.bn1 = nn.BatchNorm2d(iChannels)
        self.relu = nn.LeakyReLU()
        self.emb_size = emb_size
        self.layer1 = self._make_layer(block, cfg[0], cfg[0])
        self.layer2 = self._make_layer(block, cfg[0], cfg[1])
        self.layer3 = self._make_layer(block, cfg[1], cfg[2])
        self.layer4 = self._make_layer(block, cfg[2], cfg[3])
        self.avgpool = nn.AvgPool2d(6)
        self.maxpool = nn.MaxPool2d(kernel_size=2)
        layer_second_in_feat = cfg[2] * 5 * 5 if not cifar_flag else cfg[2] * 2 * 2
        self.layer_second = nn.Sequential(nn.Linear(in_features=layer_second_in_feat,
                                                    out_features=self.emb_size,
                                                    bias=True),
                                          nn.BatchNorm1d(self.emb_size))

        self.layer_last = nn.Sequential(nn.Linear(in_features=cfg[3],
                                                  out_features=self.emb_size,
                                                  bias=True),
                                        nn.BatchNorm1d(self.emb_size))

        for m in self.modules():
            if isinstance(m, nn.Conv2d):
                nn.init.kaiming_normal_(m.weight, mode='fan_out', nonlinearity='leaky_relu')
            elif isinstance(m, nn.BatchNorm2d):
                nn.init.constant_(m.weight, 1)
                nn.init.constant_(m.bias, 0)

    def _make_layer(self, block, inplanes, planes):
        layers = []
        layers.append(block(inplanes, planes))
        return nn.Sequential(*layers)

    def forward(self, x):
        # 3 -> 64
        x = self.conv1(x)
        x = self.bn1(x)
        x = self.relu(x)
        # 64 -> 64
        x = self.layer1(x)
        # 64 -> 128
        x = self.layer2(x)
        # 128 -> 256
        inter = self.layer3(x)
        # 256 -> 512
        x = self.layer4(inter)
        x = self.avgpool(x)
        x = x.view(x.size(0), -1)
        # 512 -> 128
        x = self.layer_last(x)
        return x


